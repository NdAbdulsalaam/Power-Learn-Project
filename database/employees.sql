-- Sample dataset: EmployeeInfo
-- Columns: EmployeeID, FirstName, LastName, Salary

-- Query to sort employees by their salary in descending order
SELECT * 
FROM EmployeeInfo 
ORDER BY Salary DESC;

/*
Purpose of Sorting:
This query sorts the employees based on their salary in descending order. 
The purpose is to identify the employees with the highest salaries at the top of the result set.
*/

